                <h1>Sobre</h1>
                <h2><i class="fas fa-caret-right"></i> O que é o Gifty?</h2>
                <p>Gifty (lê-se "gui-fi-tai") é um site com características de rede social
                    que permite que usuários criem listas de presentes vinculadas a eventos
                    particulares diversos, como casamentos, formaturas, aniversários, entre outros.</p><br>
                <h2><i class="fas fa-caret-right"></i> O que preciso fazer para começar a utilizá-lo?</h2>
                <p>Basta cadastrar-se em nosso site. Por favor, coloque um endereço de e-mail válido.</p><br>
                <h2><i class="fas fa-caret-right"></i> Por que o site quer saber os meus interesses?</h2>
                <p>Pode ser que no início fique difícil de recomendar itens para suas listas. Por este motivo,
                    você é convidado a marcar as categorias de produtos que você tem interesse. Mas isto é opcional.</p><br>
                <h2><i class="fas fa-caret-right"></i> Então o site possui sistema de recomendação?</h2>
                <p>Sim. Ele se baseia nos interesses que você marcou e nas suas atividades no site.</p><br>
                <h2><i class="fas fa-caret-right"></i> E se meus interesses mudarem?</h2>
                <p>Não há problema. O sistema de recomendação se adapta de acordo com seus novos interesses.</p><br>
                <h2><i class="fas fa-caret-right"></i> Perdi minha senha de acesso. O que faço?</h2>
                <p>Vá no link "Esqueceu a senha?", digite o seu endereço de e-mail e siga as instruções.</p><br>
                <h2><i class="fas fa-caret-right"></i> Posso criar eventos públicos?</h2>
                <p>O Gifty foi projetado para eventos particulares, em que as pessoas costumam entregar e receber
                    presentes.</p><br>
                <h2><i class="fas fa-caret-right"></i> Quantos eventos posso criar?</h2>
                <p>Você pode criar quantos quiser, mas só pode ter até 10 eventos ativos.</p><br>
                <h2><i class="fas fa-caret-right"></i> Por que preciso estabelecer um limite de compras para os convidados?</h2>
                <p>Isto evita que um convidado mal intencionado marque como comprado vários ou todos os itens, 
                    dificultando ou até impossibilitando que as demais pessoas os comprem.</p><br>
                <h2><i class="fas fa-caret-right"></i> Como o Gifty garante que os presentes não sejam repetidos?</h2>
                <p>Todos os convidados verão quais itens já foram comprados, mas nenhum deles
                    saberá quem comprou.</p><br>
                <h2><i class="fas fa-caret-right"></i> Posso excluir itens da minha lista?</h2>
                <p>Pode. Mas lembre-se que o anfitrião não sabe quais dos presentes da lista serão entregues. Caso um
                    convidado já tenha comprado o presente, você dará dor de cabeça para que ele realize uma troca.
                    Portanto, utilize este recurso em último caso. É sempre melhor ter certeza de que você realmente
                    quer o item antes de adicioná-lo na lista.</p><br>
                <h2><i class="fas fa-caret-right"></i> Posso convidar quem eu quiser para os meus eventos?</h2>
                <p>Apenas os usuários que são seus amigos no site podem ser convidados para os seus eventos.</p><br>
                <h2><i class="fas fa-caret-right"></i> Mas meus amigos ainda não utilizam o site...</h2>
                <p>Não há problema. Aqueles convidados que ainda não são usuários do site poderão receber o convite por
                    e-mail para que se cadastrem.</p><br>                
                <h2><i class="fas fa-caret-right"></i> Posso bloquear e excluir amigos?</h2>
                <p>Sim, quando você achar necessário.</p><br>                
                <h2><i class="fas fa-caret-right"></i> O Gifty é pago?</h2>
                <p>Totalmente gratuito.</p><br>
                <h2><i class="fas fa-caret-right"></i> Mas nada nesta vida é de graça...</h2>
                <p>Temos empresas parceiras que anunciam em nosso site. Não se preocupe, nada
                    que atrapalhe a sua experiência.</p><br>
